# ─── Python check ──────────────────────────────────────────────────────────────
Step "Detecting Python 3.10+..."
$py = $null
foreach ($cmd in @("python","python3","python310")) {
    try {
        $ver = & $cmd -c "import sys;print(sys.version_info[:2])" 2>$null
        if ($ver -match "\(3, (1[0-9]|[2-9]\d)") { $py = $cmd; break }
    } catch {}
}
if (-not $py) { Fail "Python 3.10+ not found. Install from https://python.org" }
OK "Python found ($py)"

# ─── Pip packages ──────────────────────────────────────────────────────────────
